from .models import Economy, It, Sports
from django.forms import ModelForm, TextInput, Textarea


class EconomyForm(ModelForm):
    class Meta:
        model = Economy
        fields = ["title", "task"]
        widgets = {
            "title": TextInput(attrs={
                'class': 'form-control',
                'placeholder': 'Введите название'
            }),
            "task": Textarea(attrs={
                'class': 'form-control',
                'placeholder': 'Введите описание'
            }),
        }


